# division always return float
number = 15 / 3
print(number)

# 加减乘除
print(1 + 2)
print(2 - 1)
print(2 * 20)

# 除法每次都会返回小数
print(10 / 5)
print(11 // 2)

# 求余
print(13 % 5)

# n次方
print(100 ** 2)

# ==========字符串============

print("\"hello world!\"")
print(r"c:\some\name")

# 字符串包含多行
print("""
    hello
    world
    多行字符串
""")

# 合并字符串
print(3 * "un" + "happy")

text = (
    "长长长长长长长长长长的字符串"
    "另一个长长长长长长长长长长的字符串"
)
print(text)

py = "Py"
print(py + "thon")

python = "Python"
print(python[0])
print(python[-6])

# 字符串切片
print(python[:2])
print(python[2:])
print(python[7:])

# 字符串长度
print(len(python))
